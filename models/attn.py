class InterpretableAttentionLayer(nn.Module):
    def __init__(self, attention, d_model, n_heads, d_keys=None,
                 d_values=None):
        super(InterpretableAttentionLayer, self).__init__()

        # TODO 加一个 assert，这里的 attention 必须是 InterpretableProbAttention 或者是 InterpretableFullAttention

        d_keys = d_keys or (d_model//n_heads)
        d_values = d_values or (d_model//n_heads)

        self.inner_attention = attention
        self.query_projection = nn.Linear(d_model, d_keys * n_heads)
        self.key_projection = nn.Linear(d_model, d_keys * n_heads)
        self.value_projection = nn.Linear(d_model, d_values * n_heads) # TFT 论文公式(14-16)中的 W_V
        self.out_projection = nn.Linear(d_values * n_heads, d_model) # TFT 论文中公式（13）中的 W_H
        self.n_heads = n_heads

    def forward(self, queries, keys, values, attn_mask):
        B, L, _ = queries.shape
        _, S, _ = keys.shape
        H = self.n_heads

        queries_h = self.query_projection(queries).view(B, L, H, -1)
        keys_h = self.key_projection(keys).view(B, S, H, -1)
        values = self.value_projection(values) # value 不用分heads，直接就映射一下，对应 TFT 论文公式(14-16)中的 W_V

        # attns.shape == (B, H, L, S), 即 (B, H, L_Q, L_K)，因为里面 transpose(1,2) 了
        attns = self.inner_attention(
            queries_h,
            keys_h,
            values,
            attn_mask
        )
        
        attn = attns.mean(dim=1)
        out =  attn @ values

        return self.out_projection(out), attn

class AttentionLayer(nn.Module):
    def __init__(self, attention, d_model, n_heads, d_keys=None,
                 d_values=None):
        super(AttentionLayer, self).__init__()

        d_keys = d_keys or (d_model//n_heads)
        d_values = d_values or (d_model//n_heads)

        self.inner_attention = attention
        self.query_projection = nn.Linear(d_model, d_keys * n_heads)
        self.key_projection = nn.Linear(d_model, d_keys * n_heads)
        self.value_projection = nn.Linear(d_model, d_values * n_heads)
        self.out_projection = nn.Linear(d_values * n_heads, d_model)
        self.n_heads = n_heads

    def forward(self, queries, keys, values, attn_mask):
        B, L, _ = queries.shape
        _, S, _ = keys.shape
        H = self.n_heads

        queries = self.query_projection(queries).view(B, L, H, -1)
        keys = self.key_projection(keys).view(B, S, H, -1)
        values = self.value_projection(values).view(B, S, H, -1)

        out, attn = self.inner_attention(
            queries,
            keys,
            values,
            attn_mask
        )
        out = out.view(B, L, -1)

        return self.out_projection(out), attn
